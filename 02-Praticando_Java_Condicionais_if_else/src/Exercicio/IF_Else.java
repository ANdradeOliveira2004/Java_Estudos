package Exercicio;

public class IF_Else {

    public static void main(String[] args) {
        int numero = 5;

        if (numero % 2 == 0) {
            System.out.println("Número  " + numero + " é Par");
        }else {
            System.out.println("Número  " + numero + " é ímpar");
        }
    }

}
